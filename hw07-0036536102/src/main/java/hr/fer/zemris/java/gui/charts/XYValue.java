package hr.fer.zemris.java.gui.charts;
/**
 * Razred koji predstavlja tocku u koordinatnom sustavu.
 * @author Petra
 *
 */
public class XYValue {
	
	int x;
	int y;
	
	public XYValue(int i, int j) {
		this.x = i;
		this.y = j;
	}
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	
	

}
